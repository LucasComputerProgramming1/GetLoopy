public class forloop_30to0 {
    public static void main(String[] args) {
        //Declaring Variables
        int cnt = 1;
        int output = cnt;
        //For Loop 30-0 Start
        for (cnt=30; cnt >= 0; cnt--)
        {
            output = cnt + 1;
            System.out.println(cnt);
        }
    }
}