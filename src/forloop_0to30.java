public class forloop_0to30 {
    public static void main(String[] args) {
        //Declaring Variables
        int cnt = 1;
        int output = cnt;
        //For Loop 0-30 Start
        for (cnt=0; cnt <= 30; cnt++)
        {
            output = cnt + 1;
            System.out.println(cnt);
        }
    }
}
