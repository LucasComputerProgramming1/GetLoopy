public class PartB_5to5 {
    public static void main(String[] args) {
        //Declaring Variables
        int row;
        int col;
        String output = "*****";
        //For Loop 5 to 5
        for (row=1; row <= 5; row++);
        {
            //Nested For Loop
            for(col=1; col<row; col++)
            {
                System.out.println(output);
            }
            System.out.println("");
        }
    }
}